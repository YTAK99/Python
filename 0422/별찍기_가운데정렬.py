# 가운데 대칭 별찍기

N = int(input())

for i in range(1, N + 1):
    print(('*' * (2 * i - 1)).center(2 * N - 1).rstrip())
# 뒤에 공백있어도 상관 없으면 rstrip()은 빼도 됨


