N = int(input())

# 위
for i in range(1, N+1):
    print(f"{'*' * (2 * i - 1):^{2 * N - 1}}")

# 아래
for i in range(N-1, 0, -1):
    print(f"{'*' * (2 * i - 1):^{2 * N - 1}}")

# ' ' * (N - i) → 가운데 맞추기
# 2*i - 1 → 별을 홀수로 만들어야 좌우 대칭

